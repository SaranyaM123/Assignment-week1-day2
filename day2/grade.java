package week1.day2;

public class grade {

	public static void main(String[] args) {
		int mark=75;
		if(mark==35) {
		System.out.println("GRADE E");
		}
		else if(mark>=50) {
		System.out.println("GRADE D");
		}
		else if(mark>65) {
		System.out.println("GRADE C");
		}
		else if(mark>=75) {
		System.out.println("GRADE B");
		}
	else {
		System.out.println("GRADE A");
	}
	}
}